          title: "Anda yakin ingin menghapus produk ini?",
          text: "Anda tidak akan dapat mengembalikan ini!",
          icon: "warning",
          buttons: true,
          dangerMode: true,
      })
      .then((willCancel) => {
          if (willCancel) {


               
              window.location.href = urlToRedirect;
             
          }  


      });

      
  }
